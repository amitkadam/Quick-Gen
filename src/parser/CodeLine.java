package parser;

public class CodeLine {

	/**
	 * @param args
	 */
	int index;
	String code;


	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
}
